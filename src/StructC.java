public class StructC {
    private String nome;
    private int index;

    public StructC(String nome, int index){
        this.nome=nome;
        this.index= index;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }
}
